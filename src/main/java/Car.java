public class Car { // класс машина
    String name = ""; // имени name присваиваем изначальное значение
    int speed = 0; // имени speed присваиваем изначальное значение

    public Car(String name, int speed) { // конструктор с параметрами name и speed.
        this.name = name; // имя машины
        this.speed = speed; // скорость машины.
    }
}